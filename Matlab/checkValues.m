E = 10;
nu = 0.3;
Lx = 20;
Ly = 20;
Lz = 20;
a = Lx/2;
b = Ly/2;
c = Lz/2;

val = E/((1+nu)*(1-2*nu));

d11 = val*(1-nu);
d44 = val*(1-2*nu)/2;
d55 = d44;
d12 = val*nu;
d22 = d11;
d23 = d12;
d33 = d11;
d13 = d12;
d66 = d44;

V = Lx*Ly*Lz;

K11 = V/8*1/9*(d11/a^2+d44/b^2+d55/c^2);
K12 = V/8*1/(6*a*b)*(d12+d44);
K13 = V/8*1/(6*a*c)*(d13+d55);
K22 = V/8*1/9*(d22/b^2+d44/a^2+d66/c^2);
K23 = V/8*1/(6*b*c)*(d23+d66);
K33 = V/8*1/9*(d33/c^2+d55/a^2+d66/b^2);

K = [K11, K12, K13; K12, K22, K23; K13, K23,K33]